<script>
function showAddCustomerModal() {
    document.getElementById('addCustomerModal').classList.remove('hidden');
}

function closeAddCustomerModal() {
    document.getElementById('addCustomerModal').classList.add('hidden');
    document.getElementById('addCustomerForm').reset();
}

document.getElementById('addCustomerForm').addEventListener('submit', async function(e) {
    e.preventDefault();
    
    const formData = new FormData(this);
    const data = Object.fromEntries(formData);
    
    try {
        const response = await App.API.post('/sales/customers/store', data);
        if (response.success) {
            App.Toast.success('Customer added successfully');
            closeAddCustomerModal();
            setTimeout(() => location.reload(), 1000);
        } else {
            App.Toast.error(response.message || 'Failed to add customer');
        }
    } catch (error) {
        App.Toast.error('An error occurred. Please try again.');
        console.error('Error:', error);
    }
});

// Search and filter functionality
document.getElementById('searchInput').addEventListener('input', filterCustomers);
document.getElementById('statusFilter').addEventListener('change', filterCustomers);

function filterCustomers() {
    const searchTerm = document.getElementById('searchInput').value.toLowerCase();
    const statusFilter = document.getElementById('statusFilter').value.toLowerCase();
    const rows = document.querySelectorAll('.customer-row');
    
    rows.forEach(row => {
        const name = row.getAttribute('data-name').toLowerCase();
        const email = row.getAttribute('data-email').toLowerCase();
        const status = row.getAttribute('data-status').toLowerCase();
        
        const matchesSearch = name.includes(searchTerm) || email.includes(searchTerm);
        const matchesStatus = !statusFilter || status === statusFilter;
        
        if (matchesSearch && matchesStatus) {
            row.style.display = '';
        } else {
            row.style.display = 'none';
        }
    });
}

function viewCustomer(id) {
    window.location.href = `/sales/customers/view/${id}`;
}

function editCustomer(id) {
    window.location.href = `/sales/customers/edit/${id}`;
}

function deleteCustomer(id) {
    App.Confirm.show({
        title: 'Delete Customer',
        message: 'Are you sure you want to delete this customer? This action cannot be undone.',
        confirmText: 'Delete',
        cancelText: 'Cancel',
        onConfirm: async () => {
            try {
                const response = await App.API.delete(`/sales/customers/delete/${id}`);
                if (response.success) {
                    App.Toast.success('Customer deleted successfully');
                    setTimeout(() => location.reload(), 1000);
                } else {
                    App.Toast.error(response.message || 'Failed to delete customer');
                }
            } catch (error) {
                App.Toast.error('An error occurred. Please try again.');
                console.error('Error:', error);
            }
        }
    });
}
</script>

